/* =====================================================
   SIRVATO OS — demo.js
   Interactive MICRO prompt builder
   ===================================================== */

document.addEventListener('DOMContentLoaded', () => {

  const fields = {
    m: document.getElementById('field-m'),
    i: document.getElementById('field-i'),
    c: document.getElementById('field-c'),
    r: document.getElementById('field-r'),
    o: document.getElementById('field-o'),
  };

  const output     = document.getElementById('demo-output');
  const generateBtn= document.getElementById('btn-generate');
  const copyBtn    = document.getElementById('btn-copy-output');
  const clearBtn   = document.getElementById('btn-clear');
  const modeSelect = document.getElementById('demo-mode');

  const FRAMEWORKS = {
    micro: {
      label: 'MICRO Rhythm Layer',
      build: (v) => buildMICRO(v),
    },
    icdf: {
      label: 'ICDF Engine',
      build: (v) => buildICDF(v),
    },
    rcr: {
      label: 'RCR-EOC Framework',
      build: (v) => buildRCR(v),
    },
    unified: {
      label: 'Unified Mode',
      build: (v) => buildUnified(v),
    },
  };

  function val(key) {
    return (fields[key] && fields[key].value.trim()) || '—';
  }

  function buildMICRO(v) {
    return [
      '╔══════════════════════════════════════════╗',
      '║         SIRVATO OS · MICRO LAYER          ║',
      '╚══════════════════════════════════════════╝',
      '',
      `M  — MESSAGE   : ${v.m || val('m')}`,
      `I² — INTENTION : ${v.i || val('i')}`,
      `C  — CONTEXT   : ${v.c || val('c')}`,
      `R  — RHYTHM    : ${v.r || val('r')}`,
      `O  — OUTPUT    : ${v.o || val('o')}`,
      '',
      '══════════════════════════════════════════',
      '  PROMPT GENERATED BY SIRVATO OS v1.0',
      '══════════════════════════════════════════',
    ].join('\n');
  }

  function buildICDF(v) {
    return [
      '╔══════════════════════════════════════════╗',
      '║         SIRVATO OS · ICDF ENGINE          ║',
      '╚══════════════════════════════════════════╝',
      '',
      `I  — INSTRUCTION : ${v.m || val('m')}`,
      `C  — CONTEXT     : ${v.c || val('c')}`,
      `D  — DATA        : ${v.i || val('i')}`,
      `F  — FORMAT      : ${v.o || val('o')}`,
      '',
      '══════════════════════════════════════════',
      '  PROMPT GENERATED BY SIRVATO OS v1.0',
      '══════════════════════════════════════════',
    ].join('\n');
  }

  function buildRCR(v) {
    return [
      '╔══════════════════════════════════════════╗',
      '║       SIRVATO OS · RCR-EOC FRAMEWORK      ║',
      '╚══════════════════════════════════════════╝',
      '',
      `R  — ROLE           : ${v.m || val('m')}`,
      `C  — CONTEXT        : ${v.c || val('c')}`,
      `R² — REQUEST        : ${v.i || val('i')}`,
      `E  — EXPECTED OUT   : ${v.o || val('o')}`,
      `O  — CONSTRAINTS    : ${v.r || val('r')}`,
      `C² — COMMUNICATION  : Professional`,
      '',
      '══════════════════════════════════════════',
      '  PROMPT GENERATED BY SIRVATO OS v1.0',
      '══════════════════════════════════════════',
    ].join('\n');
  }

  function buildUnified(v) {
    return [
      '╔══════════════════════════════════════════╗',
      '║        SIRVATO OS · UNIFIED MODE          ║',
      '╚══════════════════════════════════════════╝',
      '',
      `ROLE        : Expert AI System`,
      `INSTRUCTION : ${v.m || val('m')}`,
      `MESSAGE     : ${v.m || val('m')}`,
      `INTENTION   : ${v.i || val('i')}`,
      `REQUEST     : ${v.i || val('i')}`,
      `CONTEXT     : ${v.c || val('c')}`,
      `DATA        : [Provided inline]`,
      `EXAMPLES    : [See context]`,
      `RHYTHM      : ${v.r || val('r')}`,
      `CONSTRAINTS : Precise, factual`,
      `OUTPUT      : ${v.o || val('o')}`,
      '',
      '══════════════════════════════════════════',
      '  PROMPT GENERATED BY SIRVATO OS v1.0',
      '══════════════════════════════════════════',
    ].join('\n');
  }

  function typewrite(el, text, speed) {
    el.textContent = '';
    el.style.whiteSpace = 'pre';
    let i = 0;
    function tick() {
      if (i < text.length) {
        el.textContent += text[i++];
        setTimeout(tick, speed);
      }
    }
    tick();
  }

  function generate() {
    const mode = modeSelect ? modeSelect.value : 'micro';
    const fw = FRAMEWORKS[mode] || FRAMEWORKS.micro;
    const v = {
      m: val('m'), i: val('i'), c: val('c'), r: val('r'), o: val('o')
    };
    const text = fw.build(v);
    if (output) {
      output.style.opacity = '0';
      setTimeout(() => {
        typewrite(output, text, 12);
        output.style.transition = 'opacity 0.3s ease';
        output.style.opacity = '1';
      }, 150);
    }
  }

  if (generateBtn) generateBtn.addEventListener('click', generate);

  // Live update on input
  Object.values(fields).forEach(f => {
    if (f) f.addEventListener('input', () => {
      if (output && output.textContent && output.textContent !== '') generate();
    });
  });

  if (clearBtn) {
    clearBtn.addEventListener('click', () => {
      Object.values(fields).forEach(f => { if (f) f.value = ''; });
      if (output) { output.textContent = '// Fill in the fields above and click "Generate Prompt"'; }
    });
  }

  if (copyBtn) {
    copyBtn.addEventListener('click', () => {
      const text = output ? (output.textContent || '') : '';
      if (!text || text.startsWith('//')) return;
      navigator.clipboard.writeText(text).then(() => {
        const orig = copyBtn.textContent;
        copyBtn.textContent = '✓ Copied!';
        setTimeout(() => { copyBtn.textContent = orig; }, 2000);
      });
    });
  }

  if (modeSelect) modeSelect.addEventListener('change', generate);

  // Example presets
  document.querySelectorAll('[data-preset]').forEach(btn => {
    btn.addEventListener('click', () => {
      const preset = btn.getAttribute('data-preset');
      const PRESETS = {
        marketing: {
          m: 'Create a social media campaign for a new product launch',
          i: 'Drive brand awareness and increase conversions',
          c: 'Startup launching an AI productivity tool, B2B SaaS',
          r: 'Professional yet engaging, modern tone',
          o: 'A 5-post campaign plan with captions and hashtags',
        },
        code: {
          m: 'Refactor this React component to use TypeScript and hooks',
          i: 'Improve code quality, type safety, and maintainability',
          c: 'Production codebase, team of 5 developers',
          r: 'Technical and precise, with inline comments',
          o: 'Complete TypeScript component with prop types and tests',
        },
        analysis: {
          m: 'Analyze the competitive landscape for EdTech startups in Europe',
          i: 'Support strategic decision-making for Series A fundraising',
          c: 'Online learning platform with 10K users, pre-revenue pivot',
          r: 'Structured and data-driven, executive summary style',
          o: 'SWOT analysis + 3 strategic recommendations, 500 words',
        },
      };
      const p = PRESETS[preset];
      if (p) {
        if (fields.m) fields.m.value = p.m;
        if (fields.i) fields.i.value = p.i;
        if (fields.c) fields.c.value = p.c;
        if (fields.r) fields.r.value = p.r;
        if (fields.o) fields.o.value = p.o;
        generate();
      }
    });
  });

  // Auto-populate placeholder
  if (output) {
    output.textContent = '// Fill in the fields above and click "Generate Prompt"\n// Or try one of the example presets below';
  }
});
